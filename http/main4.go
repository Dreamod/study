package main

import (
	"fmt"
	"log"
	"net/http"
)

// **Описание**: Настройте сервер с заданным адресом :8082 и запуском через ListenAndServe, обработайте ошибку запуска с выводом в лог
//
// **Входные данные**: Нет входных данных - сервер должен запускаться и слушать указанный порт
//
// **Выходные данные**: Сервер должен успешно запуститься на порту 8082, либо вывести ошибку в лог при неудачном запуске
//
// **Ограничения**:
// - Использовать стандартную библиотеку net/http
// - Сервер должен слушать порт 8082
// - При ошибке запуска использовать log.Fatal для вывода ошибки
// - Создать простой обработчик для маршрута /status
//
// **Примеры**:
// Успешный запуск: сервер слушает на :8082 без ошибок
// Ошибка запуска: вывод ошибки через log.Fatal при занятом порте

func getStatus(w http.ResponseWriter, r *http.Request) {
	fmt.Fprintln(w, 200)
}

func main() {
	router := http.NewServeMux()
	router.HandleFunc("/status", getStatus)
	server := http.Server{
		Addr:    ":8082",
		Handler: router,
	}
	if err := server.ListenAndServe(); err != nil {
		log.Fatal(err)
	}
}
